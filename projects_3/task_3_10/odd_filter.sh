#!/bin/bash

# Скрипт для вывода нечетных чисел от 1 до 20
# С использованием continue и break

echo "=== ЧИСЛОВОЙ ФИЛЬТР ==="
echo "Вывод нечетных чисел от 1 до 20"
echo "------------------------"

# Цикл от 1 до 20
for i in {1..20}; do
    # Проверяем, не достигли ли мы числа 15
    if [ $i -eq 15 ]; then
        echo ""
        echo "🛑 Достигнуто число 15. Останавливаем цикл."
        break
    fi

    # Проверяем на четность (если число четное - пропускаем)
    if [ $((i % 2)) -eq 0 ]; then
        continue  # пропускаем четные числа
    fi

    # Если дошли до этой строки, значит число нечетное и меньше 15
    echo "✅ Нечетное число: $i"
done

echo "------------------------"
echo "✅ Работа скрипта завершена"

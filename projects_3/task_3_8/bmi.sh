#!/bin/bash

echo "=== АНАЛИЗАТОР МЕТАБОЛИЧЕСКОГО ИНДЕКСА (BMI) ==="

read -p "Введите массу тела (кг): " weight
read -p "Введите рост (метры, например 1.75): " height

# Конвертируем для целочисленного расчета
# Формула: вес / (рост * рост)
# Умножаем на 10000 чтобы избежать дробей
bmi=$((weight * 10000 / (height * 100 * height * 100 / 10000)))

echo ""
echo "=== РЕЗУЛЬТАТ ==="
echo "Масса: $weight кг"
echo "Рост: $height м"
echo "Ваш индекс массы тела (BMI): $bmi"

# Интерпретация результата
echo -n "Категория: "
if [ $bmi -lt 18 ]; then
    echo "Недостаточная масса тела"
elif [ $bmi -lt 25 ]; then
    echo "Нормальная масса тела"
elif [ $bmi -lt 30 ]; then
    echo "Избыточная масса тела"
else
    echo "Ожирение"
fi
echo "========================"

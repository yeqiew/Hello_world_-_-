#!/bin/bash

# Скрипт для вывода системной информации

echo "=== ИНФОРМАЦИЯ О СИСТЕМЕ ==="

# Имя текущей учетной записи
echo "👤 Пользователь: $USER"

# Текущее время с точностью до секунд
echo "🕐 Время: $(date +"%H:%M:%S")"

# Полный путь к рабочему каталогу
echo "📁 Рабочий каталог: $PWD"

# Количество переданных аргументов
echo "🔢 Количество аргументов: $#"

# Если есть аргументы, показываем их
if [ $# -gt 0 ]; then
    echo "📦 Переданные аргументы: $@"
fi

echo "================================"

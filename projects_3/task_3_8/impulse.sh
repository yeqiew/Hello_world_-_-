#!/bin/bash

# Скрипт для вывода информации об экспрессии гена

# Проверяем, переданы ли аргументы
if [ $# -lt 2 ]; then
    echo "Ошибка: недостаточно аргументов!"
    echo "Использование: $0 <имя_гена> <уровень_экспрессии>"
    exit 1
fi

# Сохраняем аргументы в переменные
GENE_NAME="$1"
EXPRESSION_LEVEL="$2"

# Проверяем, что уровень экспрессии - число
if ! [[ "$EXPRESSION_LEVEL" =~ ^[0-9]+$ ]]; then
    echo "Ошибка: уровень экспрессии должен быть целым числом!"
    exit 1
fi

# Выводим результат
echo "Экспрессия гена $GENE_NAME составляет $EXPRESSION_LEVEL единиц"

exit 0

#!/bin/bash

# Скрипт для проверки лог-файла и анализа кода ошибки

# Путь к лог-файлу
FILE_PATH="./system.log"

# Код ошибки (0 - нет ошибок, 1 - критическая ошибка)
ERROR_CODE=1

echo "=== ПРОВЕРКА ЛОГ-ФАЙЛА ==="

# 1. Файловый тест
if [ -f "$FILE_PATH" ]; then
    echo "✅ Лог-файл найден: $FILE_PATH"
else
    echo "❌ Ошибка: файл $FILE_PATH не существует."
fi

# 2. Анализ кода ошибки через Case
echo ""
echo "--- АНАЛИЗ КОДА ОШИБКИ ---"
echo "Код ошибки: $ERROR_CODE"

case $ERROR_CODE in
    0)
        echo "Статус: ✅ Ошибок нет."
        ;;
    1)
        echo "Статус: ❌ Критическая ошибка!"
        ;;
    *)
        echo "Статус: ⚠️ Неизвестный код: $ERROR_CODE"
        ;;
esac

# Демонстрация полезного совета про кавычки
echo ""
echo "--- ДЕМОНСТРАЦИЯ СОВЕТА ---"
EMPTY_STRING=""
echo "Сравнение с пустой строкой (с кавычками):"
if [ "$EMPTY_STRING" == "test" ]; then
    echo "Строки равны"
else
    echo "Строки не равны (работает корректно)"
fi

echo "=============================="

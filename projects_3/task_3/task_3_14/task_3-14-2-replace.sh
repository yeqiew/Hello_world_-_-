#!/bin/bash

# Скрипт для замены пробелов на табуляцию в FASTA-файле
# Заменяем пробел между заголовком (>seqX) и последовательностью на табуляцию

echo "=== ЗАМЕНА ПРОБЕЛОВ НА ТАБУЛЯЦИЮ В SEQUENCES.TXT ==="
echo ""

# Проверяем, существует ли файл
if [ ! -f "sequences.txt" ]; then
    echo "❌ Ошибка: файл sequences.txt не найден!"
    echo "Сначала создайте его командой:"
    echo '  cat > sequences.txt << EOF'
    echo '  >seq1 ATGCGTACGTTAG'
    echo '  >seq2 GGCATGCTAGCTA'
    echo '  >seq3 TTAGCGATCGTAC'
    echo '  >seq4 CCGTATGCTAGGA'
    echo '  EOF'
    exit 1
fi

# Показываем исходное содержимое
echo "📄 Исходное содержимое sequences.txt:"
echo "----------------------------------------"
cat sequences.txt
echo "----------------------------------------"
echo ""

# Показываем невидимые символы (пробелы)
echo "🔍 Отображение невидимых символов (пробелы как ^I пока нет):"
echo "   (пробелы отображаются как обычные пробелы)"
cat -A sequences.txt
echo ""

# Выполняем замену пробелов на табуляцию
echo "🔧 Выполняем замену: пробел → табуляция"
echo "   Команда: sed -i 's/ /\t/' sequences.txt"
echo "   (заменяем только первый пробел в каждой строке, где есть >)"
echo ""

# Делаем резервную копию
cp sequences.txt sequences.txt.bak
echo "✅ Создана резервная копия: sequences.txt.bak"

# Выполняем замену пробела на табуляцию (только для строк с >)
# Заменяем только первый пробел в каждой строке
sed -i '/^>/ s/ /\t/' sequences.txt

# Показываем результат
echo ""
echo "📄 Новое содержимое sequences.txt:"
echo "----------------------------------------"
cat sequences.txt
echo "----------------------------------------"
echo ""

# Показываем невидимые символы после замены
echo "🔍 Отображение невидимых символов после замены:"
echo "   (табуляция отображается как ^I)"
cat -A sequences.txt
echo ""

echo "✅ Замена успешно выполнена!"
echo "   Теперь файл готов для корректного анализа в биоинформатических утилитах"
echo "   Для восстановления используйте: cp sequences.txt.bak sequences.txt"
echo ""
echo "📊 Проверка формата:"
echo "   - До замены: пробелы между заголовком и последовательностью"
echo "   - После замены: табуляция (^I) между заголовком и последовательностью"

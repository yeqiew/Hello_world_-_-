#!/bin/bash

# Упрощенная версия скрипта (только 3 задания)

echo "=== АНАЛИЗ ОЦЕНОК СТУДЕНТОВ (УПРОЩЕННЫЙ) ==="
echo ""

# Проверка наличия файла
if [ ! -f "students.txt" ]; then
    echo "❌ Файл students.txt не найден!"
    exit 1
fi

# 1. Студенты с оценкой выше 80
echo "1) Студенты с оценкой > 80:"
awk '$2 > 80 {print "   " $1 " - " $2}' students.txt
echo ""

# 2. Студенты с оценкой ниже 70
echo "2) Студенты с оценкой < 70:"
awk '$2 < 70 {print "   " $1 " - " $2}' students.txt
echo ""

# 3. Первая строка файла
echo "3) Первая строка файла:"
awk 'NR == 1 {print "   " $0}' students.txt
echo ""

#!/bin/bash

# Скрипт для проверки прав суперпользователя

# Функция проверки прав суперпользователя
check_root() {
    if [ $EUID -eq 0 ]; then
        echo "✅ Скрипт запущен от имени суперпользователя (root)"
        return 0
    else
        echo "❌ ОШИБКА: Скрипт должен быть запущен от имени root!"
        echo "   Текущий UID: $EUID"
        echo "   Для запуска используйте: sudo ./check_root.sh"
        exit 1
    fi
}

echo "=== ПРОВЕРКА ПРАВ СУПЕРПОЛЬЗОВАТЕЛЯ ==="
echo ""
# Вызываем функцию проверки
check_root

# Основной код скрипта (выполняется только если пользователь root)
echo ""
echo "🔧 Выполняются административные задачи..."
echo "   - Чтение системных логов"
echo "   - Проверка конфигурации"
echo "   - Обновление системы"
echo ""
echo "✅ Все задачи успешно выполнены!"

exit 0

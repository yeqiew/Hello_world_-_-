# Программа для автоматизации приготовления физиологического раствора

# 1. Запрос нужного объема раствора у пользователя
volume = float(input("Введите нужный объем раствора (в мл): "))

# 2. Расчет необходимой массы соли для 0.9% концентрации
salt_mass = volume * 0.009

# 3. Объем воды ≈ общему объему (пренебрегаем объемом соли)

# 4. Вывод рецепта в файл recipe.txt
with open("recipe.txt", "w", encoding="utf-8") as file:
    file.write("ОТЧЕТ ПО ПРИГОТОВЛЕНИЮ:\n")
    file.write("-" * 23 + "\n")
    file.write(f"Общий объем: {volume:.2f} мл\n")
    file.write(f"Масса соли:  {salt_mass:.2f} г\n")
    file.write(f"Объем воды:  {volume:.2f} мл\n")

# Сообщение пользователю об успешном сохранении
print(f"Рецепт успешно сохранен в файл recipe.txt")
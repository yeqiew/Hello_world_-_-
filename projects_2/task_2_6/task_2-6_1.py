# Программа для определения типа среды по значению pH

# Принимаем значение pH от пользователя
pH = float(input("Введите значение pH: "))

# Определяем тип среды
if pH < 7:
    print("Кислая среда")
elif pH > 7:
    print("Щелочная среда")
else:  # pH == 7
    print("Нейтральная среда")

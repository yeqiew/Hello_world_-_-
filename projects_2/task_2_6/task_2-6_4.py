print("=" * 60)
print("🛡️  ВИТЯЗЬ НА РАСПУТЬЕ  🛡️")
print("=" * 60)
print("\nСтоит витязь перед камнем вещим, а на камне том надпись:")
print("\n\"Налево пойдёшь - коня потеряешь,\nНаправо пойдёшь - жизнь потеряешь,\nПрямо пойдёшь - женатому быть.\"")
print("\n" + "-" * 60)

# Выбор пути с обработкой регистра
choice = input("Куда направишь коня своего? (налево/направо/прямо): ").strip().lower()

print("\n" + "=" * 60)
print("ПРЕДСКАЗАНИЕ")
print("=" * 60 + "\n")

# Анализ выбора с использованием if/elif/else и or
if choice == "налево" or choice == "лево" or choice == "left" or choice == "л":
    print("🐎 Поехал витязь налево...")
    print("\nВыехал в чисто поле, а там — разбойники лихие!")
    print("Напали они на витязя, хотели добра лишить.")
    print("Бился витязь три дня и три ночи, отбился от врагов.")
    print("Но конь его верный в той битве пал, стрелой подлой сражённый.")
    print("\n🏆 ИТОГ: Коня потерял, но жизнь спас и честь сохранил.")
    print("   Дальше пешком пошёл, славу свою не уронил.")
    
elif choice == "направо" or choice == "право" or choice == "right" or choice == "п":
    print("⚔️ Поехал витязь направо...")
    print("\nПодъезжает к высокой горе, а там — пещера тёмная.")
    print("Из пещеры той дракон огнедышащий выползает!")
    print("Вступил витязь в бой с чудовищем лютым.")
    print("Бились они до последнего...")
    print("\n🏆 ИТОГ: Поверг витязь дракона, но и сам от ран тяжких скончался.")
    print("   Пал смертью храбрых, имя своё в веках прославив.")
    
elif choice == "прямо" or choice == "прям" or choice == "straight" or choice == "пр":
    print("💍 Поехал витязь прямо...")
    print("\nЕдет он путём-дорогой, видит — терем расписной стоит.")
    print("А в тереме том девица-красавица живёт, Василиса Премудрая.")
    print("Приглянулись они друг другу, сыграли свадьбу богатую.")
    print("Живут теперь в любви и согласии, детишек растят.")
    print("\n🏆 ИТОГ: Женатым стал, счастье семейное обрёл.")
    print("   В любви и достатке век свой прожил.")
    
else:
    print("🤔 Задумался витязь... Неведомый путь выбрал.")
    print("\nПоехал он, куда глаза глядят, через леса дремучие, через болота зыбучие.")
    print("Много приключений испытал, с чудесами невиданными встречался.")
    print("Никто не знает, что с ним стало, былин о том не сложили.")
    print("\n🏆 ИТОГ: Сгинул витязь в краях неведомых.")
    print("   Или, может, обрёл то, что искал? Тайна сия велика есть...")

print("\n" + "=" * 60)
print("НА ТОМ СКАЗАНИЮ КОНЕЦ")
print("=" * 60)